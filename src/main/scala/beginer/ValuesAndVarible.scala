package beginer

object ValuesAndVarible extends App {

  val x:Int  = 42

  println("value of x = "+x)

  val x1 = 44
  println(x1)

  val aString : String = "a String value ";

  println(aString)
// boolean type
  val aBoolean : Boolean = true

  println(aBoolean)

// char type

  val aChar : Char = 'a'
  println(aChar)

  //Short type

  val aShort : Short = 123
  println(aShort)

  // Long Type

  val aLong : Long = 123L
  println(aLong)

  //Float Type
  val aFloat : Float = 12.11f

  println(aFloat)

  // Double type
  val aDouble : Double = 123.34d
  println(aDouble)


  // Variables

  var aVar : Int = 12
  println("Var value 1 "+ aVar)

  aVar = 1234
  println("Var value 2 " + aVar)


}
